<h1 class="font-weight-bold d-flex justify-content-center mb-5" style="font-size: 3.5rem;">
   Politique de confidentialite
</h1>

<div class="barSection">
   <hr>
</div>
<div class="taille">

<h2 class="font-weight-bold mt-5 mb-5" >1 – Définitions</h2>
<p class="mb-5"><strong>« Données à caractère personnel »</strong> ou<strong> « Données » </strong>désigne toute information se rapportant à une personne physique identifiée ou identifiable. Est réputée être une « personne physique identifiable » une personne physique qui peut être identifiée, directement ou indirectement, notamment par référence à un identifiant, tel qu'un nom, un numéro d'identification, des Données de localisation, un identifiant en ligne, ou à un ou plusieurs éléments spécifiques propres à son identité physique, physiologique, génétique, psychique, économique, culturelle ou sociale ;<br />
   <strong>« Données sensibles » </strong>désigne les Données qui révèlent l'origine raciale ou ethnique, les opinions politiques, les convictions religieuses ou philosophiques ou l'appartenance syndicale, ainsi que le traitement des Données génétiques, des Données biométriques aux fins d'identifier une personne physique de manière unique, des Données concernant la santé ou des Données concernant la vie sexuelle ou l'orientation sexuelle d'une personne physique. L'association ne traite aucune donnée sensible;<br />
   <strong>« Responsable du traitement » </strong>désigne la personne physique ou morale, l'autorité publique, le service ou un autre organisme qui, seul ou conjointement avec d'autres, détermine les finalités et les moyens du traitement. En l’espèce il s’agit de l'association Les Hautes-Bornes;<br />
   <strong>« Règlementation » </strong>désigne le Règlement n° 2016/679 du Parlement européen et du Conseil du 27 avril 2016 relatif à la protection des personnes physiques à l’égard du traitement des Données à caractère personnel et à la libre circulation des Données (RGPD) et la Loi informatique et liberté n°78-17 du 6 janvier 1978 modifiée.<br />
   <strong>« Sites » </strong>désigne tous les sites internet édités par Les Hautes-Bornes.</p>

<h2 class="font-weight-bold mb-5">2 – Les Données que nous recueillons</h2>
<p class="mb-5 ">Par l’intermédiaire de note Site internet, nous sommes susceptibles de recueillir et de traiter les Données suivantes :

<h6 class="font-weight-bold mb-5">2.1 – Les Données que nous recueillons à l’occasion de vos reservation et/ou par le biais de notre formulaire de <a href="{{ route('chemin_contact') }}#map">Contacts</a> </h6>

<p>Lorsque vous vous inscrivez à un événement ou contribuer au financement d’un événement nous recueillons également les Données ci-dessus ainsi qu’une photographie si vous nous en fournissez une.
Si vous nous transmettez des informations personnelles concernant un tiers, vous devrez lui fournir une copie de cette Politique. </p>

<h6 class="font-weight-bold  mb-5">2.2 – Les Données que nous recueillons à l’occasion de votre inscription à notre newsletter ou de la signature d’une pétition</h6>
<p>Lors de votre inscription à notre newsletter, nous recueillons les Données suivantes :

<ul>
 
   <li> <p> Nom ;</p> </li>
   <li> <p> Prénom ;</p></li>
   <li> <p> Email ;</p></li>
   <li> <p> Numéro de téléphone.</p></li>
</ul>

<p class="mb-5">Ces Données nous permettrons de vous contacter par email, courrier, téléphone ou sms pour vous faire part des actualités de l'association, faire appel à votre générosité, vous inviter à des événements….</p>

<h2 class="font-weight-bold mb-5">2.3 – Les Données que nous recueillons via les cookies</h2>

<p class="mb-5">Nous ne recueillons que les cookies strictement nécessaire pour le bon fonctionnement du site web, aucun tiers n'est utilisé à se jour.</p> 

<h2 class="font-weight-bold mb-5">2.4 – Les Données sensibles</h2>

<p class="mb-5" >Les Hautes-Bornes ne traite aucune Donnée réellement sensible (voir 2.2).</p>

<h2 class="font-weight-bold mb-5">3 – Les durées de conservations</h2>
<p>Les Données des donateurs seront conservées pendant 6 ans après l’année du dernier don, notamment pour être en mesure de répondre aux demandes de l’administration fiscale en cas de contrôle. Pendant cette période, votre adresse email pourra être utilisée afin de vous faire part des actualités de l'association, faire appel à votre générosité, vous inviter à des événements.Au-delà de cette période, les données anonymisées permettant la réalisation d’études statistiques seront alors conservées.</p>
<p class="mb-5">Chaque traitement de Données poursuit une finalité précise et est fondé sur une base légale.</p>



<h2 class="font-weight-bold mb-5">4 – Les destinataires de vos Données</h2>
<p class="mb-5" >Peuvent être destinataires de vos Données :</p>

<ul>
   <li> les Services « Communication » et « Générosité Publique » de Les Hautes-Bornes vous adresser nos actualités ou de possible demandes de dons ;</li>
   <li>le service « Générosité Publique » de Les Hautes-Bornes de la gestion de la base de données des donateurs et utilisateur des nos fomulaires ;</li>
   <li>nos prestataires informatiques(pour le moment nous même);</li>
</ul>

<p class="mb-5">
En cas de réorganisation ou de restructuration, envisagées ou finalisées, certains auditeurs, conseillers, acquéreurs, pourraient également avoir accès à vos Données.
Il peut également arriver que vos Données soient partagées avec d’autres associations. Vous pouvez vous y opposer selon la méthode visée à l’article 9.</p>




<h2 class="font-weight-bold mb-5">5 – Transferts de Données</h2>
<p>
Les Hautes-Bornes ne transfère pas vos Données en dehors de l’Union européenne. Néanmoins, il est a ce jour impossible que ces dernières soient hébergées sur des serveurs situés en dehors de l’Union européenne. Serveur hébergé par la société O2switch dont le siège social se situe 222 Boulevard Gustave-Flaubert - 63000 Clermont-Ferrand.</p>


<p>
Dans ce cas, Les Hautes-Bornes s’engage à prendre toutes les mesures nécessaires à la préservation de l’intégrité et de la sécurité de vos Données afin que ces dernières soient :</p>


<p class="mb-5">
situées dans un Etat considéré comme adéquat par la Commission européenne ;
envoyées à des entités présentant des garanties suffisantes quant à la protection des Données à caractère personnel par la mise en œuvre et la signature de clauses contractuelles types approuvées par la Commission européenne.</p>


<h2 class="font-weight-bold mb-5">6 – Sécurité des Traitements</h2>
<p>
Les Hautes-Bornes assure le traitement de vos Données en toute confidentialité et en toute sécurité en mettant en place les mesures techniques et organisationnelles nécessaires pour préserver dans des conditions optimales leur sécurité, leur confidentialité et leur intégrité.</p>


<p class="mb-5">
Seules les personnes strictement habilitées par Les Hautes-Bornes auront accès à vos Données. Aucun tiers non autorisé ne saurait y avoir accès, hors obligation légale ou unioniste incombant au Responsable de traitement.</p>


<h2 class="font-weight-bold mb-5">7 - Journalistes, influenceurs/utilisateurs des réseaux sociaux, experts</h2>
<p class="mb-5">
Les Hautes-Bornes tient à jour les coordonnées (disponibles publiquement) des journalistes et personnes qui exercent une influence sur les réseaux sociaux, les experts et personnes qui rendent publics leurs renseignements personnels ou professionnels ou leurs opinions. Ces listes sont consultées et/ou utilisées par Les Hautes-Bornes afin d’adresser des communiqués de presse et des informations sur la défense des causes qui lui sont chères. La base légale de ce traitement est l’intérêt légitime de Les Hautes-Bornes.</p>


<h2 class="font-weight-bold mb-5">8 – Vos droits</h2>
<p>
En application de la loi Informatique et libertés et du RGPD, vous disposez à tout moment d'un droit d'accès, de modification, d’opposition à la prospection commerciale et en cas de motifs légitimes, du droit à la limitation du traitement de vos Données et du droit de demander la suppression des Données personnelles vous concernant. Vous disposez également du droit à la portabilité de vos Données, du droit de définir des directives concernant vos Données en cas de décès et du droit de retirer votre consentement. Vous pouvez exercer vos droits en écrivant depuis notre formulaire de <a href="{{ route('chemin_contact') }}#map">Contacts</a>  . </p>


<p class="mb-5">
Si satisfaction ne vous était pas donnée concernant le traitement de vos Données ou l’exercice de vos droits, vous avez la possibilité de saisir la CNIL.</p>


<h2 class="font-weight-bold mb-5">9 – Mise à jour </h2>
<p class="mb-5">
La présente politique pourra faire l’objet d’actualisation par Les Hautes-Bornes, en cas de nouveaux traitements, et notamment sur le fondement :</p>

<ul class="mb-5"> 
   <li> <p> des changements significatifs intervenus dans la législation et/ou la jurisprudence ainsi que les normes techniques applicables au Système d’Information ;</p></li>
   <li><p>de l’évaluation des risques ;</p></li>
   <li><p>des réajustements suggérés par la mise en œuvre des procédures internes de gestion de l’information.</p></li>
</ul>

